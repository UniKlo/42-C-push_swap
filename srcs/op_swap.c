#include "../incl/ch_ps.h"

t_stack	*sa(t_stack *a)
{
	if (!a->tag || !a)
		return (NULL);
	int tmp;
	tmp = a->nxt->nbr;
	a->nxt->nbr = a->nbr;
	a->nbr = tmp;

	return (a);
}
